	e21:SetTarget(aux.TargetBoolFunction(Card.IsAttribute,ATTRIBUTE_EARTH))
	e21:SetValue(c511000079.atkval3)
	c:RegisterEffect(e21)
	local e22=e4:Clone()
	e22:SetCondition(c511000079.becon3)
	c:RegisterEffect(e22)
end
function c511000079.atval(c,att)
	return c:IsAttackable() and c:IsAttribute(att) 
		and Duel.IsExistingMatchingCard(c511000079.adval,Duel.GetTurnPlayer(),0,LOCATION_MZONE,1,nil,att)
end
function c511000079.adval(c,att)
	return c:IsFaceup() and c:IsAttribute(att)
end

function c511000079.initial_effect(c)
	--Activate
	local e1=Effect.CreateEffect(c)
	e1:SetType(EFFECT_TYPE_ACTIVATE)
	e1:SetCode(EVENT_FREE_CHAIN)
	c:RegisterEffect(e1)
	--must attack (light)
	local e2=Effect.CreateEffect(c)
	e2:SetType(EFFECT_TYPE_FIELD)
	e2:SetCode(EFFECT_MUST_ATTACK)
	e2:SetRange(LOCATION_SZONE)
	e2:SetTargetRange(LOCATION_MZONE,LOCATION_MZONE)
	e2:SetTarget(aux.TargetBoolFunction(Card.IsAttribute,ATTRIBUTE_LIGHT))
	c:RegisterEffect(e2)
	local e3=Effect.CreateEffect(c)
	e3:SetType(EFFECT_TYPE_FIELD)
	e3:SetCode(EFFECT_MUST_BE_ATTACKED)
	e3:SetTargetRange(LOCATION_MZONE,LOCATION_MZONE)
	e3:SetRange(LOCATION_SZONE)
	e3:SetTarget(aux.TargetBoolFunction(Card.IsAttribute,ATTRIBUTE_LIGHT))
	e3:SetValue(c511000079.atkval1)
	c:RegisterEffect(e3)
	local e4=Effect.CreateEffect(c)
	e4:SetType(EFFECT_TYPE_FIELD)
	e4:SetCode(EFFECT_CANNOT_EP)
	e4:SetRange(LOCATION_SZONE)
	e4:SetProperty(EFFECT_FLAG_PLAYER_TARGET)
	e4:SetTargetRange(1,1)
	e4:SetCondition(c511000079.becon1)
	c:RegisterEffect(e4)
	local e5=e2:Clone()
	e5:SetTarget(aux.TargetBoolFunction(Card.IsAttribute,ATTRIBUTE_DARK))
	c:RegisterEffect(e2)
	local e6=e3:Clone()
	e6:SetTarget(aux.TargetBoolFunction(Card.IsAttribute,ATTRIBUTE_DARK))
	e6:SetValue(c511000079.atkval2)
	c:RegisterEffect(e6)
	local e7=e4:Clone()
	e7:SetCondition(c511000079.becon2)
	c:RegisterEffect(e7)
	local e8=e2:Clone()
	e8:SetTarget(aux.TargetBoolFunction(Card.IsAttribute,ATTRIBUTE_WATER))
	c:RegisterEffect(e8)
	local e9=e3:Clone()
	e9:SetTarget(aux.TargetBoolFunction(Card.IsAttribute,ATTRIBUTE_WATER))
	e9:SetValue(c511000079.atkval4)
	c:RegisterEffect(e9)
	local e10=e4:Clone()
	e10:SetCondition(c511000079.becon4)
	c:RegisterEffect(e10)
	local e11=e2:Clone()
	e11:SetTarget(aux.TargetBoolFunction(Card.IsAttribute,ATTRIBUTE_FIRE))
	c:RegisterEffect(e11)
	local e12=e3:Clone()
	e12:SetTarget(aux.TargetBoolFunction(Card.IsAttribute,ATTRIBUTE_FIRE))
	e12:SetValue(c511000079.atkval5)
	c:RegisterEffect(e12)
	local e13=e4:Clone()
	e13:SetCondition(c511000079.becon5)
	c:RegisterEffect(e13)
	local e14=e2:Clone()
	e14:SetTarget(aux.TargetBoolFunction(Card.IsAttribute,ATTRIBUTE_WIND))
	c:RegisterEffect(e14)
	local e15=e3:Clone()
	e15:SetTarget(aux.TargetBoolFunction(Card.IsAttribute,ATTRIBUTE_WIND))
	e15:SetValue(c511000079.atkval6)
	c:RegisterEffect(e15)
	local e16=e4:Clone()
	e16:SetCondition(c511000079.becon6)
	c:RegisterEffect(e16)
	local e17=e2:Clone()
	e17:SetTarget(aux.TargetBoolFunction(Card.IsAttribute,ATTRIBUTE_DEVINE))
	c:RegisterEffect(e17)
	local e18=e3:Clone()
	e18:SetTarget(aux.TargetBoolFunction(Card.IsAttribute,ATTRIBUTE_DEVINE))
